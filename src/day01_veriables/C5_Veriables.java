package day01_veriables;

class C5_Veriables {

	public static void main(String[] args) {
		
		int sayi1=10;
	    int sayi2=4;
	    double sayi3=4;
	    
	    System.out.println(sayi1/sayi2); // ikisi de int oldugu icin tam sayi kabul eder

	    System.out.println(sayi1/sayi3); // sayi3 double oldugu icin sonucu ondalik olur 
	    
	    System.out.println(sayi1+sayi2);
	    System.out.println(sayi1+sayi2+sayi3);
	    System.out.println(sayi1*sayi2);
	    System.out.println(sayi1*sayi2*sayi3);
	}

} 
